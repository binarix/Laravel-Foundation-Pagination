<?php

namespace Chromabits\FoundationPagination;

use Illuminate\Support\ServiceProvider;

/**
 * Class FoundationPaginationServiceProvider
 *
 * @package Chromabits\FoundationPagination
 */
class FoundationPaginationServiceProvider extends ServiceProvider
{
    /**
     * Indicates if loading of the provider is deferred.
     *
     * @var bool
     */
    protected $defer = false;

    /**
     * Register the service provider.
     *
     * @return void
     */
    public function register()
    {
        $this->app['view']->addNamespace('foundationpagination', __DIR__ . '/views');
    }

    /**
     * Get the services provided by the provider.
     *
     * @return array
     */
    public function provides()
    {
        return array();
    }
}